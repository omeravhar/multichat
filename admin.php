            var socket = io.connect("ws://127.0.0.1:2022");
            let adminSocketId = "";
            
            //set admin user
            $(function() {
                socket.emit('add admin user', "admin");
            });
               


            $('.message').on('keypress', function(event){
                if(event.key === "Enter"){
                    let messageData = {
                    socket:adminSocketId,
                    message:$(this).val()
                    };
                    socket.emit('send message', messageData);
                    console.log(messageData);
                    $(this).val('');
                }
                
            });


//adding users to admin panel 
            socket.on('login', function(data){
                let lastUser = data.numUsers-1;
                $("#chatModalLabel").text("chat with "+data.users[lastUser].name);
                
                
                $('#usersOnChat').append(
                `<li class="nav-item m-3">
                <span data-feather="home"  class="align-text-bottom"></span>
                <button type="button"
                 class="btn btn-primary" 
                 data-bs-toggle="modal" 
                 data-bs-target="#chatModal"
                 chat_id="`+data.users[lastUser].socketId+`" 
                 name_id="`+data.users[lastUser].name+`"   
                 >
                 `+data.users[lastUser].name+`
                 </button>
                </li>`);

                    
            });
            
            
            socket.on('new message', function(data){
                console.log(data);
                $('#chat-messages').append('<p>' + data +'</p>');
            });
            


            socket.on('admin login', function(data){
                adminSocketId  = data.users["admin"];
                console.log(adminSocketId);
            });
            
            
    </script>
